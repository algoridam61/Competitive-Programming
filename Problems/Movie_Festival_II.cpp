void solve()
{
    ll n;cin>>n;
    ll k;cin>>k;
    
    vpl start_times;
    vpl finish_times;
    vpl actual;
    f(i,n)
    {
        ll x,y;cin>>x>>y;
        start_times.push_back({x,i});
        finish_times.push_back({y,i});
        actual.push_back({x,y});
    }

    sortall(finish_times);

    ordered_set_pair<ll,ll> k_friends;

    f(i,k)
    {
        k_friends.insert({0,i});
    }
    ll missed = 0;
    
    f(i,n)
    {
        ll curr_idx = finish_times[i].second;
        ll curr_start = start_times[curr_idx].first;
        ll curr_finish = finish_times[i].first;

        // find if any friend first value is less that equal to curr_start
        if (k_friends.order_of_key({curr_start,1e10})>0)
        {
            // get the friend with the minimum first value
            auto it = k_friends.find_by_order(k_friends.order_of_key({curr_start,1e10})-1);
            ll friend_idx = it->second;
            k_friends.erase(it);
            k_friends.insert({curr_finish,friend_idx});
        }
        else missed++;
    }

    cout << n - missed << endl;






    
    
}
