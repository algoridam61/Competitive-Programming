void solve()
{
    ll n_ll; cin >> n_ll;
    ll q_ll; cin >> q_ll;

    int n = static_cast<int>(n_ll);
    int q = static_cast<int>(q_ll);
    ll LOGN = 32;

    vector<vector<int>> ancestors(n + 1, vector<int>(LOGN, 0));

    for (int i = 2; i <= n; ++i)
    {
        int boss; cin >> boss;
        ancestors[i][0] = boss;
    }

    for (int j = 1; j < LOGN; ++j)
    {
        for (int i = 1; i <= n; ++i)
        {
            if (ancestors[i][j-1] != 0)
            {
               ancestors[i][j] = ancestors[ancestors[i][j-1]][j-1];
            }
        }
    }

    while (q--)
    {
        int x; cin >> x;
        int k; cin >> k;
        // x = 2;
        // k = 15;

        for (int j = LOGN - 1; j >= 0; --j) 
        {
            if (x == 0) break;
            if ((k >> j) & 1) 
            {
                x = ancestors[x][j];
            }
        }

        if (x == 0) 
        {
            cout << -1; L;
        }
        else
        {
            cout << x; L;
        }
    }
}
