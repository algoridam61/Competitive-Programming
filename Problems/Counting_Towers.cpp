ll dp1[1000001];
ll dp2[1000001];
void solve()
{
    ll n;cin>>n;

    
    // an = 2*an-1 + bn-1
    // bn = bn-1 + 2*bn-1 + an-1+bn-1 = 4*bn-1 + an-1

    dp1[1] = 1;
    dp2[1] = 1;
    for(ll i=2;i<=n;i++)
    {
        dp1[i] = (2*dp1[i-1] + dp2[i-1])%MOD;
        dp2[i] = (4*dp2[i-1] + dp1[i-1])%MOD;
    }
    ll ans = (dp1[n] + dp2[n])%MOD;
    cout<<ans<<endl;




    
}
